#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int c;

	i = ft_strlen(s1);
	c = 0;
	while (s2[c])
	{
		s1[i + c] = s2[c];
		c++;
	}
	s1[i + c] = '\0';
	return (s1);
}
